package graphics;

public class Scroller{
    public final int x;
    public final int y;
    public final int w;
    public final int h;
    public int subx;

    public Scroller(int x, int y, int w, int h, int subx){
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
        this.subx = subx;
    }
}
